pub fn mg_to_g(val: f64) -> f64 {
    val
}

pub fn g_to_mg(val: f64) -> f64 {
    val
}

pub fn kg_to_g(val: f64) -> f64 {
    val * 1000.0
}

pub fn g_to_kg(val: f64) -> f64 {
    val / 1000.0
}

pub fn t_to_g(val: f64) -> f64 {
    val * 1000000.0
}

pub fn g_to_t(val: f64) -> f64 {
    val / 1000000.0
}

pub fn oz_to_g(val: f64) -> f64 {
    val * 28.3495
}

pub fn g_to_oz(val: f64) -> f64 {
    val / 28.3495
}

pub fn lb_to_g(val: f64) -> f64 {
    val * 453.592
}

pub fn g_to_lb(val: f64) -> f64 {
    val / 453.592
}

pub fn t_metric_to_g(val: f64) -> f64 {
    val * 1000000000.0
}

pub fn g_to_t_metric(val: f64) -> f64 {
    val / 1000000000.0
}
